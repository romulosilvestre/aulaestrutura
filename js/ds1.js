function showNumbers() {
    let contador = 0 //inicio
    while (contador < 10) { //condição
        alert(contador) //algoritmo
        contador = contador + 1 //incremento
    }
}

function showNumbersFor(){    
   //ETAPA 1 - inicio 
   //ETAPA 2 - condição
   //ETAPA 3 - algoritmo
   //ETAPA 4 - incremento  
   for(let contador=0;contador<10;contador++){              
       alert(contador) 
   }
   
}
